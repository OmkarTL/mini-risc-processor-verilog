module risc_top(
    input clk,
    input reset,
    output [7:0] led
);

////////////////////////////////////////////////////////////
// INTERNAL WIRES
////////////////////////////////////////////////////////////

wire slow_clk;

wire [7:0] pc;
wire [15:0] instruction;

wire [3:0] opcode;
wire [1:0] rd;
wire [1:0] rs1;
wire [1:0] rs2;

wire [1:0] alu_sel;
wire reg_write;

wire [7:0] read_data1;
wire [7:0] read_data2;

wire [7:0] alu_result;

////////////////////////////////////////////////////////////
// INSTRUCTION DECODE
////////////////////////////////////////////////////////////

assign opcode = instruction[15:12];
assign rd     = instruction[11:10];
assign rs1    = instruction[9:8];
assign rs2    = instruction[7:6];

////////////////////////////////////////////////////////////
// CLOCK DIVIDER
////////////////////////////////////////////////////////////

clock_divider CD (
    .clk(clk),
    .reset(reset),
    .slow_clk(slow_clk)
);

////////////////////////////////////////////////////////////
// PROGRAM COUNTER
////////////////////////////////////////////////////////////

program_counter PC (
    .clk(slow_clk),
    .reset(reset),
    .pc(pc)
);

////////////////////////////////////////////////////////////
// INSTRUCTION MEMORY
////////////////////////////////////////////////////////////

instruction_memory IM (
    .address(pc),
    .instruction(instruction)
);

////////////////////////////////////////////////////////////
// CONTROL UNIT
////////////////////////////////////////////////////////////

control_unit CU (
    .opcode(opcode),
    .alu_sel(alu_sel),
    .reg_write(reg_write)
);

////////////////////////////////////////////////////////////
// REGISTER FILE
////////////////////////////////////////////////////////////

register_file RF (
    .clk(slow_clk),
    .we(reg_write),
    .read_reg1(rs1),
    .read_reg2(rs2),
    .write_reg(rd),
    .write_data(alu_result),
    .read_data1(read_data1),
    .read_data2(read_data2)
);

////////////////////////////////////////////////////////////
// ALU
////////////////////////////////////////////////////////////

alu ALU (
    .A(read_data1),
    .B(read_data2),
    .ALU_Sel(alu_sel),
    .Result(alu_result)
);

////////////////////////////////////////////////////////////
// FPGA DEBUG OUTPUT
////////////////////////////////////////////////////////////

// Display Program Counter on LEDs
assign led = pc;

// Alternative:
// assign led = alu_result;

endmodule